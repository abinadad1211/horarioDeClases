
package Modelo;

public class Recipiente{
    
    protected String nombreCompleto;
    protected String matricula;
    protected String experienciaEducativa;
    protected String diaLunes;
    protected String diaMartes;
    protected String diaMiercoles;
    protected String diaJueves;
    protected String diaViernes;
    protected String diaSabado;
    protected String folioHorario;
    protected String programaEducativo;
    protected String grupo;
    protected String nrc;

        public Recipiente() {
    }
    
    public String getNrc() {
        return nrc;
    }

    public void setNrc(String nrc) {
        this.nrc = nrc;
    }
  
    public String getNombreCompleto() {
        return nombreCompleto;
    }

    public void setNombreCompleto(String nombreCompleto) {
        this.nombreCompleto = nombreCompleto;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String getExperienciaEducativa() {
        return experienciaEducativa;
    }

    public void setExperienciaEducativa(String experienciaEducativa) {
        this.experienciaEducativa = experienciaEducativa;
    }

    public String getDiaLunes() {
        return diaLunes;
    }

    public void setDiaLunes(String diaLunes) {
        this.diaLunes = diaLunes;
    }

    public String getDiaMartes() {
        return diaMartes;
    }

    public void setDiaMartes(String diaMartes) {
        this.diaMartes = diaMartes;
    }

    public String getDiaMiercoles() {
        return diaMiercoles;
    }

    public void setDiaMiercoles(String diaMiercoles) {
        this.diaMiercoles = diaMiercoles;
    }

    public String getDiaJueves() {
        return diaJueves;
    }

    public void setDiaJueves(String diaJueves) {
        this.diaJueves = diaJueves;
    }

    public String getDiaViernes() {
        return diaViernes;
    }

    public void setDiaViernes(String diaViernes) {
        this.diaViernes = diaViernes;
    }

    public String getDiaSabado() {
        return diaSabado;
    }

    public void setDiaSabado(String diaSabado) {
        this.diaSabado = diaSabado;
    }

    public String getFolioHorario() {
        return folioHorario;
    }

    public void setFolioHorario(String folioHorario) {
        this.folioHorario = folioHorario;
    }

    public String getProgramaEducativo() {
        return programaEducativo;
    }

    public void setProgramaEducativo(String programaEducativo) {
        this.programaEducativo = programaEducativo;
    }

    public String getGrupo() {
        return grupo;
    }

    public void setGrupo(String grupo) {
        this.grupo = grupo;
    }
 
    
}
